"""
Returns functions for basic arithmetic
"""

def add(x,y):
    """
    Adds given numbers
    """
    return x + y

def divide(x,y):
    """
    Divides given numbrs
    """
    return x / y

def multiply(x,y):
    """
    Multiplies given numbers
    """
    return x * y

def subtract(x,y):
    """
    Subtracts given numbers
    """
    return x - y

